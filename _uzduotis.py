# Pratęsimas: Darbuotojų Valdymo Sistema su Skyriais ir Projektais

# Praplėskite pradinę darbuotojų programą, pridėdami šiuos funkcionalumus:

# 1. Skyrių valdymas:
#    - Sukurkite galimybę pridėti/redaguoti įmonės skyrius (pavadinimas, vadovas, vieta)
#    - Kiekvienas darbuotojas priklauso vienam skyriui (One-to-Many ryšys)
#    - Galimybė peržiūrėti visus skyriaus darbuotojus
#    - Galimybė perkelti darbuotoją į kitą skyrių

# 2. Projektų valdymas:
#    - Sukurkite projektų sistemą (pavadinimas, pradžios data, pabaigos data, statusas)
#    - Darbuotojai gali dirbti keliuose projektuose vienu metu (Many-to-Many ryšys)
#    - Galimybė priskirti/pašalinti darbuotojus iš projektų
#    - Galimybė peržiūrėti:
#      * Visus projekto darbuotojus
#      * Visus darbuotojo projektus